// ─── 判定函数 ──────────────────────────────────────────────────────

export function determineConstitution(monthZhi: number, dayGan: number, day?: number): ConstitutionItem | null {
  const key = getKey(monthZhi, dayGan, day);
  const item = constitutionMap[key];
  if (!item) return null;
  const el = ELEMENT_MAP[dayGan];
  return {
    ...item,
    season: getSeason(monthZhi, day),
    masterFive: el,
    matchGod: getDefaultMatchGod(el),
    avoidGod: getDefaultAvoidGod(el),
    illnessTip: getDefaultIllness(el),
  };
}

export function getConstitutionName(monthZhi: number, dayGan: number): string {
  const item = determineConstitution(monthZhi, dayGan);
  return item ? item.name : '未匹配';
}
