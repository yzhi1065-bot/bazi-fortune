// ─── 月令地支 → 季节映射 ──────────────────────────────────────
// 寅卯辰=春 巳午未=夏 申酉戌=秋 亥子丑=冬
// 辰(清明后)、戌(寒露后)、丑(小寒后)、未(小暑后)上半月属本季，下半月属季土
/**
 * 四立前各12天（约）为四季土旺日，辰戌丑未下半月为季土
 * 上半月：辰=春、未=夏、戌=秋、丑=冬
 * 下半月(约15日起)：辰=季、未=季、戌=季、丑=季
 * 若不传day参数，默认按上半月(本季)处理
 */
function getSeason(mz: number, day?: number): string {
  const halfMap: Record<number, {first:string;second:string;splitDay:number}> = {
    4: {first:'春', second:'季', splitDay:20},  // 辰月(清明) 20日前后为界
    7: {first:'夏', second:'季', splitDay:23},  // 未月(小暑) 23日前后为界
    10:{first:'秋', second:'季', splitDay:23}, // 戌月(寒露) 23日前后为界
    1: {first:'冬', second:'季', splitDay:20},  // 丑月(小寒) 20日前后为界
  };
  if (day !== undefined && halfMap[mz]) {
    return day <= halfMap[mz].splitDay ? halfMap[mz].first : halfMap[mz].second;
  }
  const simple: Record<number, string> = {
    0:'冬',1:'冬',2:'春',3:'春',4:'春',
    5:'夏',6:'夏',7:'夏',8:'秋',9:'秋',10:'秋',11:'冬'
  };
  return simple[mz] || '季';
}

